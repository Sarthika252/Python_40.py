def assignment_operations():
    # Initial value
    a = 10
    print(f"Initial value of a: {a}")

    # Assigning new value
    a = 5
    print(f"a = 5 -> a: {a}")

    # Addition assignment
    a += 3  # equivalent to a = a + 3
    print(f"a += 3 -> a: {a}")

    # Subtraction assignment
    a -= 2  # equivalent to a = a - 2
    print(f"a -= 2 -> a: {a}")

    # Multiplication assignment
    a *= 4  # equivalent to a = a * 4
    print(f"a *= 4 -> a: {a}")

    # Division assignment
    a /= 2  # equivalent to a = a / 2
    print(f"a /= 2 -> a: {a}")

    # Modulus assignment
    a %= 3  # equivalent to a = a % 3
    print(f"a %= 3 -> a: {a}")

    # Exponentiation assignment
    a **= 2  # equivalent to a = a ** 2
    print(f"a **= 2 -> a: {a}")

    # Floor division assignment
    a //= 3  # equivalent to a = a // 3
    print(f"a //= 3 -> a: {a}")
assignment_operations()